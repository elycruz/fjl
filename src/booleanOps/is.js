export const

    /**
     * Returns whether `value` is 'truthy' or not
     * @function module:booleanOps.isTruthy
     * @param value
     * @returns {Boolean}
     */
    isTruthy = value => !!value,

    /**
     * Returns whether `value` is 'falsy' or not
     * @function module:booleanOps.isFalsy
     * @param value
     * @returns {Boolean}
     */
    isFalsy = value => !value;
